      <p>
        Understanding the overseas dimensions of British universities during the
        gap between the medieval European academic community and the
        internationalism of the twentieth century has gained new salience due to
        investigations into universities’ historical connections to enslavement
        and the slave trade. This movement began in the USA – at Brown
        University in 20065 – and by 2014, over 100 universities in north
        America and beyond had formed the consortium ‘Universities Studying
        Slavery’.6 British universities have taken note, and at least 17 have
        begun similar studies; and, at the time of writing, at least nine have
        published some form of report, starting with Glasgow in 2019. The
        existence of a digital version of the Biographical Register of the
        University of St Andrews, containing records from the University’s
        matriculation registers from 1579-1897, gives us the opportunity to
        generate some high-level statistics on the student body. Analysis of the
        Biographical Register has enabled us to identify some of the students
        who were born in the colonies (though we do not know where they were
        brought up). These figures offer some insight into the diversity (or
        not) of experiences and attitudes that would have been present in the
        student community. They also point to another way in which the
        University benefited financially from colonialism, via the fees paid by
        the parents of students from (or associated with) the colonies. We have
        also been able to identify some of the alumni who later spent time in
        the colonies, for personal or professional reasons. These figures reveal
        the ways in which St Andrews helped educate people who became part of
        the military or administrative systems underpinning empire, or who used
        the opportunities offered by empire to find outlets for their
        professional or business skills.
